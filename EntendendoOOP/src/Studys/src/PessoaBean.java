package Studys.src;

public class PessoaBean {
	
	private String nome;
	private int idade;
	
	public PessoaBean() {
	
	}
	
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getNome() {
		return nome;
	}
	
	
	public void setIdade(int idade) {
		this.idade = idade;
	}
	public int getIdade() {
		return idade;
	}
}
